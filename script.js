document.addEventListener('DOMContentLoaded', () => {
    // 1. Sticky Header
    const header = document.getElementById('header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // 2. Animated Counters
    const counters = document.querySelectorAll('.counter');
    const speed = 200; // The lower the slower

    const animateCounters = () => {
        counters.forEach(counter => {
            const updateCount = () => {
                const target = +counter.getAttribute('data-target');
                const count = +counter.innerText.replace(/,/g, '');
                const inc = target / speed;

                if (count < target) {
                    counter.innerText = Math.ceil(count + inc).toLocaleString('en-US');
                    setTimeout(updateCount, 10);
                } else {
                    counter.innerText = target.toLocaleString('en-US');
                }
            };
            updateCount();
        });
    };

    // Use Intersection Observer to trigger counter animation when visible
    const impactSection = document.querySelector('.impact-section');
    let hasAnimated = false;

    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.5
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting && !hasAnimated) {
                animateCounters();
                hasAnimated = true;
            }
        });
    }, observerOptions);

    if (impactSection) {
        observer.observe(impactSection);
    }

    // 3. Scroll Reveal Animation
    const reveals = document.querySelectorAll('.reveal');

    const revealOnScroll = () => {
        const windowHeight = window.innerHeight;
        const elementVisible = 150;

        reveals.forEach(reveal => {
            const elementTop = reveal.getBoundingClientRect().top;
            if (elementTop < windowHeight - elementVisible) {
                reveal.classList.add('active');
            }
        });
    };

    window.addEventListener('scroll', revealOnScroll);
    revealOnScroll(); // Trigger once on load

    // 4. Language Translation Logic
    const langToggleBtn = document.getElementById('langToggle');

    function setLanguage(lang) {
        currentLang = lang;
        localStorage.setItem('lang', lang);
        
        // Change document direction and lang
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        
        // Update toggle button text
        const langText = langToggleBtn.querySelector('.lang-text');
        if (langText) {
            langText.textContent = lang === 'ar' ? 'EN' : 'AR';
        }

        // Update logo based on layout direction
        const mainLogo = document.getElementById('main-logo');
        if (mainLogo) {
            if (lang === 'en') {
                mainLogo.src = 'https://res.cloudinary.com/dca2x8jje/image/upload/v1778407726/%D8%B4%D8%B9%D8%A7%D8%B1%D8%A7%D9%84%D9%85%D8%A4%D8%B3%D8%B3%D8%A9_%D9%85%D9%81%D8%B1%D8%BA-%D8%B9%D8%B1%D8%B6%D9%8A_%D9%8A%D8%B3%D8%A7%D8%B1_xxdpg6.png';
            } else {
                mainLogo.src = 'https://res.cloudinary.com/dca2x8jje/image/upload/v1778276253/%D8%B4%D8%B9%D8%A7%D8%B1%D8%A7%D9%84%D9%85%D8%A4%D8%B3%D8%B3%D8%A9_-_%D9%85%D9%81%D8%B1%D8%BA_-_%D8%B9%D8%B1%D8%B6%D9%8A_zpwch7.png';
            }
        }

        // Translate text elements
        document.querySelectorAll('[data-translate]').forEach(el => {
            const key = el.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                el.textContent = translations[lang][key];
            }
        });

        // Swap arrow direction classes
        document.querySelectorAll('.arrow-icon').forEach(icon => {
            if(lang === 'en') {
                icon.classList.remove('fa-arrow-left');
                icon.classList.add('fa-arrow-right');
            } else {
                icon.classList.remove('fa-arrow-right');
                icon.classList.add('fa-arrow-left');
            }
        });
        
        // Re-trigger scroll animations manually so layout fixes itself if needed
        if (typeof revealOnScroll === 'function') {
            revealOnScroll();
        }
    }

    if (langToggleBtn) {
        langToggleBtn.addEventListener('click', (e) => {
            e.preventDefault();
            setLanguage(currentLang === 'ar' ? 'en' : 'ar');
        });
    }

    // Initialize language on load
    setLanguage(currentLang);

    // 5. Hero Mouse Parallax Effect
    const hero = document.getElementById('hero');
    const heroBgWrapper = document.getElementById('heroBgWrapper');

    if (hero && heroBgWrapper) {
        hero.addEventListener('mousemove', (e) => {
            const x = (e.clientX / window.innerWidth - 0.5) * 30; // max 30px movement
            const y = (e.clientY / window.innerHeight - 0.5) * 30;
            // Negative value to move opposite to mouse direction for better 3D feel
            heroBgWrapper.style.transform = `translate(${-x}px, ${-y}px)`;
        });

        hero.addEventListener('mouseleave', () => {
            heroBgWrapper.style.transform = `translate(0, 0)`;
        });
    }

    // 6. Mobile Menu Toggle
    const mobileMenuToggle = document.getElementById('mobileMenuToggle');
    const navMenu = document.getElementById('navMenu');
    const mobileMenuOverlay = document.getElementById('mobileMenuOverlay');

    if (mobileMenuToggle && navMenu && mobileMenuOverlay) {
        const toggleMenu = () => {
            navMenu.classList.toggle('active');
            mobileMenuOverlay.classList.toggle('active');
            // Toggle SVG icons
            const iconBars = mobileMenuToggle.querySelector('.icon-bars');
            const iconClose = mobileMenuToggle.querySelector('.icon-close');
            if (iconBars && iconClose) {
                if (navMenu.classList.contains('active')) {
                    iconBars.style.display = 'none';
                    iconClose.style.display = 'block';
                } else {
                    iconBars.style.display = 'block';
                    iconClose.style.display = 'none';
                }
            }
        };

        mobileMenuToggle.addEventListener('click', toggleMenu);
        mobileMenuOverlay.addEventListener('click', toggleMenu);

        // Close menu when clicking a link
        const navLinks = document.querySelectorAll('.nav-links a');
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                if (navMenu.classList.contains('active')) {
                    toggleMenu();
                }
            });
        });
    }

    // 7. Partners Tabs Toggle
    const partnerTabs = document.querySelectorAll('.partner-tab');
    const partnerSliders = document.querySelectorAll('.partners-slider');

    if (partnerTabs.length > 0 && partnerSliders.length > 0) {
        partnerTabs.forEach(tab => {
            tab.addEventListener('click', () => {
                // Remove active from all tabs
                partnerTabs.forEach(t => t.classList.remove('active'));
                // Add active to clicked tab
                tab.classList.add('active');

                // Hide all sliders
                partnerSliders.forEach(slider => {
                    slider.style.display = 'none';
                    slider.classList.remove('active');
                });

                // Show target slider
                const targetId = tab.getAttribute('data-target');
                const targetSlider = document.getElementById(targetId);
                if (targetSlider) {
                    targetSlider.style.display = 'block';
                    // small timeout for CSS transition if any
                    setTimeout(() => targetSlider.classList.add('active'), 10);
                }
            });
        });
    }
});

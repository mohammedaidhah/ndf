document.addEventListener('DOMContentLoaded', () => {
    // 1. Sticky Header
    const header = document.getElementById('header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // 2. Animated Counters
    const counters = document.querySelectorAll('.counter');
    const speed = 200; // The lower the slower

    const animateCounters = () => {
        counters.forEach(counter => {
            const updateCount = () => {
                const target = +counter.getAttribute('data-target');
                const count = +counter.innerText.replace(/,/g, '');
                const inc = target / speed;

                if (count < target) {
                    counter.innerText = Math.ceil(count + inc).toLocaleString('en-US');
                    setTimeout(updateCount, 10);
                } else {
                    counter.innerText = target.toLocaleString('en-US');
                }
            };
            updateCount();
        });
    };

    // Use Intersection Observer to trigger counter animation when visible
    const impactSection = document.querySelector('.impact-section');
    let hasAnimated = false;

    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.5
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting && !hasAnimated) {
                animateCounters();
                hasAnimated = true;
            }
        });
    }, observerOptions);

    if (impactSection) {
        observer.observe(impactSection);
    }

    // 3. Scroll Reveal Animation
    const reveals = document.querySelectorAll('.reveal');

    const revealOnScroll = () => {
        const windowHeight = window.innerHeight;
        const elementVisible = 150;

        reveals.forEach(reveal => {
            const elementTop = reveal.getBoundingClientRect().top;
            if (elementTop < windowHeight - elementVisible) {
                reveal.classList.add('active');
            }
        });
    };

    window.addEventListener('scroll', revealOnScroll);
    revealOnScroll(); // Trigger once on load

    // 4. Language Translation Logic
    let currentLang = 'ar';
    const langToggleBtn = document.getElementById('langToggle');

    langToggleBtn.addEventListener('click', (e) => {
        e.preventDefault();
        currentLang = currentLang === 'ar' ? 'en' : 'ar';
        
        // Change document direction and lang
        document.documentElement.lang = currentLang;
        document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
        
        // Translate text elements
        document.querySelectorAll('[data-translate]').forEach(el => {
            const key = el.getAttribute('data-translate');
            if (translations[currentLang][key]) {
                el.textContent = translations[currentLang][key];
            }
        });

        // Swap arrow direction classes
        document.querySelectorAll('.arrow-icon').forEach(icon => {
            if(currentLang === 'en') {
                icon.classList.remove('fa-arrow-left');
                icon.classList.add('fa-arrow-right');
            } else {
                icon.classList.remove('fa-arrow-right');
                icon.classList.add('fa-arrow-left');
            }
        });
        
        // Re-trigger scroll animations manually so layout fixes itself if needed
        revealOnScroll();
    });

    // 5. Hero Mouse Parallax Effect
    const hero = document.getElementById('hero');
    const heroBgWrapper = document.getElementById('heroBgWrapper');

    if (hero && heroBgWrapper) {
        hero.addEventListener('mousemove', (e) => {
            const x = (e.clientX / window.innerWidth - 0.5) * 30; // max 30px movement
            const y = (e.clientY / window.innerHeight - 0.5) * 30;
            // Negative value to move opposite to mouse direction for better 3D feel
            heroBgWrapper.style.transform = `translate(${-x}px, ${-y}px)`;
        });

        hero.addEventListener('mouseleave', () => {
            heroBgWrapper.style.transform = `translate(0, 0)`;
        });
    }

    // 6. Mobile Menu Toggle
    const mobileMenuToggle = document.getElementById('mobileMenuToggle');
    const navMenu = document.getElementById('navMenu');
    const mobileMenuOverlay = document.getElementById('mobileMenuOverlay');

    if (mobileMenuToggle && navMenu && mobileMenuOverlay) {
        const toggleMenu = () => {
            navMenu.classList.toggle('active');
            mobileMenuOverlay.classList.toggle('active');
            // Toggle icon between bars and times (close)
            const icon = mobileMenuToggle.querySelector('i');
            if (icon.classList.contains('fa-bars')) {
                icon.classList.remove('fa-bars');
                icon.classList.add('fa-times');
            } else {
                icon.classList.remove('fa-times');
                icon.classList.add('fa-bars');
            }
        };

        mobileMenuToggle.addEventListener('click', toggleMenu);
        mobileMenuOverlay.addEventListener('click', toggleMenu);

        // Close menu when clicking a link
        const navLinks = document.querySelectorAll('.nav-links a');
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                if (navMenu.classList.contains('active')) {
                    toggleMenu();
                }
            });
        });
    }
});
